# Documenting your code
# 
# Adding comments to your code is extremely important 
# to make sure that you and others can understand what your code is about. 
# 
# R makes use of the # sign to add comments, just like Twitter!
# 
# It's important to note that comments are not run as R code, 
# so they will not influence your result. 
# For example, Calculate 3 + 4 in the editor on the right is a comment 
# and is ignored during execution.


# Calculate 3 + 4
3 + 4

# Calculate 6 + 12
6 + 12